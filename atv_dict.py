def adicionar_contato(agenda, nome, telefone):
    agenda[nome] = telefone
    print(f"Contato {nome} adicionado com sucesso.")

def buscar_contato(agenda, nome):
    if nome in agenda:
        print(f"Telefone de {nome}: {agenda[nome]}")
    else:
        print(f"Contato {nome} não encontrado.")

def remover_contato(agenda, nome):
    if nome in agenda:
        del agenda[nome]
        print(f"Contato {nome} removido com sucesso.")
    else:
        print(f"Contato {nome} não encontrado para remoção.")

def listar_contatos(agenda):
    if not agenda:
        print("Agenda vazia.")
        return
    print("--- Contatos na Agenda ---")
    for nome in sorted(agenda.keys()):
        print(f"{nome}: {agenda[nome]}")
    print("--------------------------")

