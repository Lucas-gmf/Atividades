"""Exercício 12 – Contando ocorrências
Dada a string frase = 'o rato roeu a roupa do rei de roma'
escreva um programa que conte quantas vezes cada palavra aparece.
Use um dicionário onde a chave é a palavra e o valor é a contagem.
Dica: use frase.split() para obter a lista de palavras.
Saída esperada (pode variar a ordem):
  {'o': 2, 'rato': 1, 'roeu': 1, 'a': 1, 'roupa': 1, 'do': 1, 'rei': 1, 'de': 1, 'roma': 1}


"""

frase = 'a rata roeu a roupa do rei de roma'

palavras = frase.split()

contagem = {}

for palavra in palavras:
    if palavra in contagem:
        contagem[palavra] += 1
    else:
        contagem[palavra] = 1

print(contagem)

